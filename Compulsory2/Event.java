public class Event {
    private String Name;
    private int Size;
    private int Start;
    private int End;

    public Event() {
    }

    public void setName(String name) {this.Name = name;}
    public void setSize(int Size) { this.Size = Size; }
    public void setStart(int Start) { this.Start = Start; }
    public void setEnd(int End) { this.End = End; }


}
